use crate::expr::{ExprEntry, ExprMatcher};

use super::super::candidate::{Candidate, GroupKey};
use super::super::expand::expand_entries;
use super::eval_choice::resolve_multinode_choices;
use super::eval_consume::eval_consume;
use super::eval_fetch::fetch_produced;
use super::eval_inner::EvalContext;
use super::eval_shared::{bind_produced_into_scope, into_static_scoped, visible_kind_map};
use crate::runtime_impl::env::Scope;

/// Expands `entries` against work_scope, consumes children for each expanded candidate using
/// first-match, descends into each consumed child exactly once, and writes produced into `sink`.
///
/// Expanded candidates are accumulated into `all_expanded` for use in cover/counts/post-loop checks.
/// A for-source can reference previously accumulated ids in work_scope (backward-only).
/// After processing, the produced from this body is also reflected into the Γ_set of `work_scope`
/// to make it visible for for-source reference resolution of subsequent siblings
/// (equivalent to the sequential bind in the old produce_entries_incremental).
pub(super) fn eval_dir_file(
    ctx: &mut EvalContext,
    entries: &[ExprEntry],
    work_scope: &mut Scope,
    sink: &mut crate::runtime_impl::record_map::RecordMap,
) {
    // Collect produced generated by the body locally first. Because expand_entries holds an immutable
    // borrow on work_scope during expansion, all binds into work_scope are deferred until after the
    // loop (after the borrow is released).
    let mut body_produced = crate::runtime_impl::record_map::RecordMap::new();

    // Expansion is performed against the current Γ_set of work_scope. While expand_entries holds
    // an immutable borrow on work_scope, no mutations occur; writes go only to body_produced inside
    // the loop.
    let expanded = expand_entries(ctx.tree, entries, work_scope, ctx.rules);
    let expanded = resolve_multinode_choices(
        ctx.tree,
        expanded,
        ctx.rules,
        ctx.path,
        ctx.rule_name,
        ctx.rule_chain,
        ctx.errors,
        ctx.memo,
    );

    for scoped in expanded {
        let global_index = ctx.all_expanded.len();
        let (entry_cow, scope_cow, origin, owner) = &scoped;
        let entry = entry_cow.as_ref();
        let entry_scope: &Scope = scope_cow.as_ref();

        match &entry.matcher {
            ExprMatcher::Choice { body, .. } => {
                for (alt_idx, alt) in body.iter().enumerate() {
                    let candidate = Candidate {
                        entry: alt,
                        scope: entry_scope,
                        group_key: Some(GroupKey {
                            group_index: global_index,
                            alt_index: alt_idx,
                        }),
                        origin,
                        entry_index: global_index,
                    };
                    eval_consume(ctx, &candidate, *owner, &mut body_produced);
                }
            }
            ExprMatcher::Dir { .. } | ExprMatcher::File { .. } => {
                let candidate = Candidate {
                    entry,
                    scope: entry_scope,
                    group_key: None,
                    origin,
                    entry_index: global_index,
                };
                eval_consume(ctx, &candidate, *owner, &mut body_produced);
            }
            ExprMatcher::Fetch { .. } => {
                // Fetch is observation-only and does not consume children, but its id set is reflected into body_produced.
                let fetch_sets = fetch_produced(ctx.tree, entry, entry_scope);
                for (id, records) in fetch_sets {
                    body_produced.entry(id).or_default().extend(records);
                }
            }
            ExprMatcher::Match { .. }
            | ExprMatcher::For { .. }
            | ExprMatcher::Group { .. }
            | ExprMatcher::Use { .. }
            | ExprMatcher::Value { .. } => {
                unreachable!("expanded entries contain only Dir/File/Choice/Fetch");
            }
        }
        ctx.all_expanded.push(into_static_scoped(scoped));
    }

    // Reflect body produced into both sink (parent produced or wrapper local sink) and work_scope.
    // The reflection into work_scope makes the ids from this body visible to for-source references
    // in subsequent sibling entries.
    // Since produced ids carry no kind, look up each id's producer kind from the entries' visible kind map.
    let kind_of = visible_kind_map(entries);
    for (id, records) in body_produced {
        bind_produced_into_scope(work_scope, &kind_of, &id, &records);
        sink.entry(id).or_default().extend(records);
    }
}
